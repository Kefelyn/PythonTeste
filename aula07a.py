n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
soma = n1+n2
subtracao = n1-n2
divisao = n1/n2
divisao2 = n1//n2
potenciacao = n1**n2
resto = n1%n2
multiplicacao = n1*n2
raiz = (n1+n2)**(1/2)

print(f'A soma vale {soma}, a subtração é {subtracao}, a divisao é {divisao}')
print(f'a divisão inteira é {divisao2}, a potenciação é {potenciacao}, o resto é {resto}')
print(f'a multiplicação é {multiplicacao} e a raiz quadrada é {raiz}')